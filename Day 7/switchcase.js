let fruits = "Apple";
switch(fruits) {
    case "Apple" : console.log("Apple : Rs. 120");
    break;
    case "Mango" : console.log("Mango : Rs. 140");
    break;
    case "Banana" : console.log("Banana : Rs. 20");
    break;
    case "Grapes" : console.log("Grapes : Rs. 220");
    break;
    case "Cherry" : console.log("Cherry : Rs. 420");
    break;
    default : console.log("Invalid Input");
}